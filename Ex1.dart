class Carro{
  String placa="";
  String cor=""; 
  int ano=0;
  Carro(String placa,String cor , int ano){
    this.placa = placa;
    this.cor   = cor;
    this.ano   = ano;
  }
  toString(){
    return "Placa $placa Cor: $cor Ano: $ano";
  }
}




